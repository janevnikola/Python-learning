#Basic Kalkulator programa vo Python
broj1=input("Vnesi go prviot broj: ") #vo broj1 ke go zacuvame inputot, tekstot e samo text koj se pecati na ekran
broj2=input("Vnesi go vtoriot broj: ")
rezultat=int(broj1)+int(broj2)
print("Sobiranje: "+str(rezultat))

rezultat=int(broj1)-int(broj2)
print("Odzemanje: "+str(rezultat))

rezultat=int(broj1)*int(broj2)
print("Mnozenje: "+str(rezultat))

rezultat=int(broj1)/int(broj2)
print("Delenje: "+str(rezultat))

rezultat=int(broj1)%int(broj2)
print("Ostatok: "+str(rezultat))
